package com.ccdc.robot.domain;

public class InitParam {
    private Float pan;
    private Float tilt;
    private Float zoom;

    public Float getPan() {
        return pan;
    }

    public void setPan(Float pan) {
        this.pan = pan;
    }

    public Float getTilt() {
        return tilt;
    }

    public void setTilt(Float tilt) {
        this.tilt = tilt;
    }

    public Float getZoom() {
        return zoom;
    }

    public void setZoom(Float zoom) {
        this.zoom = zoom;
    }
} 
